	public String generate() {
		StringBuffer phone = new StringBuffer();
		Random r = new Random();
		int i = r.nextInt(3);
		int length = 0;
		switch (i) {
		// 一般地区：区号+7位固话
		case 0:
			length = AREA.length;
			phone.append(getPrefix(AREA[r.nextInt(length)]));
			phone.append(getSuffix(7));
			break;
		// 区号+8位固话
		case 1:
			length = AREA_NUM.length;
			phone.append(getPrefix(AREA_NUM[r.nextInt(length)]));
			phone.append(getSuffix(8));
			break;
		// 不包括区号,7位或8位固话
		case 2:
			phone.append(getSuffix(randomInt(7, 8)));
			break;
		default:
			// do nothing
		}
		return phone.toString();
	}

	private String getPrefix(String area) {
		String[] a = area.split("\\|");
		return a[1];
	}

	private String getSuffix(int length) {
		StringBuffer phone = new StringBuffer();
		phone.append(randomInt(2, 8));// 固话以2-8开头
		for (int i = 0; i < length - 1; i++) {
			phone.append(RANDOM.nextInt(10));
		}
		return phone.toString();
	}
